  getEvents(){
    this.ebs.getEvents().subscribe(e => {
      console.log(e.events);
      e.events.forEach(event => {
        this.newEvent = {
          start: addHours(new Date(event.start.local), 0),
          end: addHours(new Date(event.end.local), 2),
          title: event.name.text,
          color: colors.blue
        }
        this.events.push(this.newEvent);
        this.refresh.next();
      });
    })
  }

  getModalData() {
    this.ebs.getEvents().subscribe(e => {
      e.events.forEach(event => {
        this.dojoEvent = {
          title: event.name.text,
          start: event.start.local,
          end: event.end.local,
          description: event.description.text,
          location: event.venue.address.localized_address_display,
          eventUrl: event.url,
          img: event.logo.url,
          locationHREF: 'http://www.google.com/maps/place/' + event.venue.latitude + ',' + event.venue.longitude
        };
        this.dojoEvents.push(this.dojoEvent);
        console.log(this.dojoEvents);
      });
    });
  }

  dayClicked({ date, events }: { date: Date; events: CalendarEvent[] }): void {
    if (isSameMonth(date, this.viewDate)) {
      if (
        (isSameDay(this.viewDate, date) && this.activeDayIsOpen === true) ||
        events.length === 0
      ) {
        this.activeDayIsOpen = false;
      } else {
        this.activeDayIsOpen = true;
      }
      this.viewDate = date;
    }
  }

  eventTimesChanged({
    event,
    newStart,
    newEnd
  }: CalendarEventTimesChangedEvent): void {
    this.events = this.events.map(iEvent => {
      if (iEvent === event) {
        return {
          ...event,
          start: newStart,
          end: newEnd
        };
      }
      return iEvent;
    });
    this.handleEvent('Dropped or resized', event);
  }

